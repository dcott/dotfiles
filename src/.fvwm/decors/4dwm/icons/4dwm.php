<h3>About my theme</h3>
Tested on fvwm-2.5.24 and fvwm-2.5.21, Ubuntu 7.10, Kubuntu 7.10 and Windows (cygwin).
<ul>
<li>Buttons layout is based on vectors<br>
<br><center><img src="buttons.png"></center><br>
<li>Pager has two virtual desktop<br>
<br><center><img src="pager.png"></center><br>
<li>Toolchest has static menu with standard Gnome applications<br>
<br><center><img src="toolchest.png"></center><br>
<li>Icons of minimized windows have Irix-like background to get more similar look<br>
<br><center><img src="icons.png"></center><br>
<li>Pager and toolchest are sticky,  they go to active desktop
<li>It should run on every system with fvwm (even MS Windows with Cygwin installed)
</ul>
<br>
If you want try some more realistic Irix environment on your Linux, try <a href="http://www.5dwm.org">MaXX Interactive Desktop</a>. It's still unfinished but quite usable and looks better (based on improved motif). Remember, MaXX is not free, for more info check MaXX site.
