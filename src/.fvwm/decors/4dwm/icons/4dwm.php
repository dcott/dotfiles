<table width=90% bgcolor=white align=center><tr><td>
We all know <a href="http://www.fvwm.org">fvwm</a>, window manager with huge amount of configuration parameters. In our rcfile we can controll most things by changing default settings from simple twm-like to most complex <a href="http://fvwm-crystal.org">Crystal</a> with transparency and other modern stuff.<br>
I like Irix look, so I try to get 4dwm from fvwm. 
